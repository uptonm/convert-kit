export function getConverter(group: string, slug: string): ConverterDef | undefined {
  return CONVERTERS.find((c) => c.group === group && c.slug === slug);
}

export function convertersByGroup(group: string): ConverterDef[] {
  return CONVERTERS.filter((c) => c.group === group);
}

export function popularConverters(): ConverterDef[] {
  return CONVERTERS.filter((c) => c.popular && c.status === "live");
}

export function searchConverters(query: string): ConverterDef[] {
  const q = query.trim().toLowerCase();
  if (!q) return CONVERTERS;
  return CONVERTERS.filter((c) => {
    const hay = `${c.title} ${c.from} ${c.to} ${c.description} ${c.subgroup ?? ""} ${c.group}`.toLowerCase();
    return hay.includes(q);
  });
}

export function converterHref(c: ConverterDef): string {
  return `/${c.group}/${c.slug}`;
}
