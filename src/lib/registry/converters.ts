import type { ConverterDef, Engine, Ambition, ConverterStatus, InputMode, GroupSlug } from "./types";

type PartialConv = {
  from: string;
  to: string;
  group: GroupSlug;
  subgroup?: string;
  engine: Engine;
  ambition?: Ambition;
  status?: ConverterStatus;
  inputMode?: InputMode;
  accept?: string;
  maxBytes?: number;
  maxDurationSec?: number;
  popular?: boolean;
  notes?: string;
  description?: string;
  bidirectional?: boolean;
};

function slugPair(from: string, to: string) {
  const f = from.toLowerCase().replace(/[^a-z0-9]+/g, "-");
  const t = to.toLowerCase().replace(/[^a-z0-9]+/g, "-");
  return { forward: `${f}-to-${t}`, reverse: `${t}-to-${f}` };
}

function make(p: PartialConv): ConverterDef[] {
  const { forward, reverse } = slugPair(p.from, p.to);
  const ambition = p.ambition ?? "core";
  const status = p.status ?? (ambition === "stretch" ? "coming-soon" : "live");
  const inputMode = p.inputMode ?? "file";
  const base = {
    group: p.group,
    subgroup: p.subgroup,
    engine: p.engine,
    ambition,
    status,
    inputMode,
    accept: p.accept,
    maxBytes: p.maxBytes,
    maxDurationSec: p.maxDurationSec,
    popular: p.popular,
    notes: p.notes,
  };

  const forwardDef: ConverterDef = {
    ...base,
    id: `${p.group}/${forward}`,
    slug: forward,
    title: `${p.from} to ${p.to}`,
    description:
      p.description ?? `Convert ${p.from} to ${p.to} in your browser. Files stay on your device.`,
    from: p.from,
    to: p.to,
    reverseSlug: p.bidirectional === false ? undefined : reverse,
  };

  if (p.bidirectional === false) return [forwardDef];

  const reverseDef: ConverterDef = {
    ...base,
    id: `${p.group}/${reverse}`,
    slug: reverse,
    title: `${p.to} to ${p.from}`,
    description: `Convert ${p.to} to ${p.from} in your browser. Files stay on your device.`,
    from: p.to,
    to: p.from,
    reverseSlug: forward,
    popular: false,
    accept: acceptForFormat(p.to),
  };

  return [forwardDef, reverseDef];
}

function one(
  from: string,
  to: string,
  group: GroupSlug,
  engine: Engine,
  extra: Omit<PartialConv, "from" | "to" | "group" | "engine"> = {},
): ConverterDef[] {
  return make({ from, to, group, engine, ...extra });
}

